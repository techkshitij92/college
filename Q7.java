import java.util.*;
public class Q7 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Input : ");
        int a = sc.nextInt();
        int b = sc.nextInt();
        System.out.println("Output :");
        System.out.println("++a : "+ ++a);
        System.out.println("b++ : "+ b++);
        System.out.println("--b : "+ --b);
        System.out.println("a-- : "+ a--);
        sc.close();
    }
    
}
