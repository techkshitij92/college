public class Q14 {
    public static void main(String[] args) {
        System.out.println("Using do...while Loop");
        int i = 1;
        do {
            System.out.println(+i);
            i++;
        }while(i<=10);
        System.out.println("Using while Loop");
        i = 1;
        while(i<=10){
            System.out.println(i);
            i++;
        }
    }
    
}
